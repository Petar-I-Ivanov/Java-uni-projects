package pu.fmi.web.uplow.model;

public class TryInfo {
	
	private int guess;
	private String message;
	
	public void setGuess(int guess) {
		this.guess = guess;
	}
	
	public int getGuess() {
		return this.guess;
	}
	
	public void setMessage(String message) {
		this.message = message;
	}
	
	public String getMessage() {
		return this.message;
	}
}